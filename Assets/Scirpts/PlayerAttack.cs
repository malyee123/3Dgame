    void AttackWithLockedTarget()
    {
        if (!IsTargetInRange(currentTarget))
        {
            currentTarget = FindNearestEnemyInRange();
        }

        if (currentTarget == null)
        {
            Debug.Log($"[Player {spawnIndex}魹ｽ魹ｽ | 魹ｽ魹ｽﾄ｡: {transform.position}] 魹ｽ魹ｽﾅｸ魹ｽ 魹ｽﾈｿ魹ｽ Enemy 魹ｽ魹ｽ魹ｽ魹ｽ");
            return;
        }

        EnemyHealth health = currentTarget.GetComponent<EnemyHealth>();

        if (health == null)
        {
            currentTarget = null;
            return;
        }

        float distance = Vector2.Distance(transform.position, currentTarget.transform.position);
        health.TakeDamage(attackDamage);

        Debug.Log($"[Player {spawnIndex}魹ｽ魹ｽ | 魹ｽ魹ｽﾄ｡: {transform.position}] " +
